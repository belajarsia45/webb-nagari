    <h2 style="color: #c62828; text-align: center; font-size: 28px;">Sejarah Nagari Talang Babungo</h2>
    <hr style="width: 80px; border: 2px solid #fdd835; margin: 10px auto 30px;" />
 
<center> <img src="img nagari/sejarah.jpg" alt="sejarah" style="max-width: 100%; height: auto; border-radius: 12px; box-shadow: 0 2px 8px rgba(0,0,0,0.1);"></center><br>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Konon kabarnya enam abad yang lalu, terjadi perpindahan penduduk dari daerah Kubung Tigo Baleh ke daerah Sirukam. Dari sana, perjalanan dilanjutkan menuju aliran Batang Gumanti. Rombongan yang datang ini dipimpin oleh beberapa tokoh penting, yaitu:
    </p>

    <ul style="color: #2c3e50; font-size: 16px; line-height: 1.8; padding-left: 20px;">
      <li>Datuk Rajo Nan Gadang</li>
      <li>Datuk Pituan Rajo Magek</li>
      <li>Datuk Rajo Nan Sati</li>
      <li>Ninik Nan Retek</li>
      <li>Ninik Nan Basa</li>
      <li>Gadih Longgah (Cayo Allah)</li>
    </ul>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Tujuan perpindahan ini adalah untuk mencari daerah baru yang layak untuk diolah dan ditempati oleh anak cucu mereka di kemudian hari. Daerah Kubung Tigo Baleh saat itu sudah padat dan terbentuk keturunan, serta lahan persawahannya terbatas.
    </p>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Setelah meninggalkan Sirukam, mereka melewati hutan lebat dan anak sungai. Sungai ini kemudian dikenal sebagai <strong>Palangkahan Ninik Nan Berenam</strong>, dan sampai sekarang dinamai <strong>Palangki</strong>. Perjalanan berlanjut ke arah timur hingga mereka tiba di sebuah daerah yang sekarang dikenal sebagai <strong>Talaok</strong> (dalam Kenagarian Sarik Alahan Tigo), di mana mereka mulai membuka ladang, persawahan, dan mendirikan dusun.
    </p>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Namun, para pemimpin rombongan belum merasa puas dan melanjutkan perjalanan menyusuri aliran sungai yang dulu mereka namai <strong>Batang Tandenan</strong> (sekarang Batang Gumanti), hingga akhirnya mereka sampai di daerah yang kini dikenal sebagai <strong>Talang Babungo</strong>.
    </p>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Sebelum menetap, mereka kembali ke Sirukam untuk menjemput sanak saudara. Sebelum berangkat, <strong>Gadih Longgah (Cayo Allah)</strong> menemukan serumpun <em>talang (buluh)</em> yang sedang berbunga. Karena menyukai bunga talang itu, ia membawanya pulang ke Sirukam sebagai tanda mata.
    </p>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Sesampainya di Sirukam, Gadih Longgah dengan bangga menyampaikan bahwa mereka telah menemukan daerah yang baik untuk dihuni. Ia berkata bahwa ia telah “menjelajahi <strong>Talang nan Babungo Balum Baunian</strong>”, sembari menunjukkan bunga talang yang dibawanya. Sejak saat itu, daerah tersebut dikenal dengan nama <strong>Talang Babungo</strong>.
    </p>

    <p style="color: #2c3e50; font-size: 16px; line-height: 1.8; text-align: justify;">
      Hingga kini, hubungan antara Talang Babungo dan daerah asal seperti Sirukam tetap terjaga, dan kisah ini terus diwariskan dari generasi ke generasi sebagai bagian dari identitas dan sejarah Nagari Talang Babungo.
    </p>
